// 설계 원칙
//  1) 컴포넌트 테이블에 "중간값"을 두지 않는다. 평균을 두 번 내면 무조건 중앙으로 수렴하므로,
//     각 컴포넌트가 극단값을 가져야 최종 점수가 벌어진다.
//  2) 마지막에 게인(gain)으로 분산을 강제 확장한다. spreadTotals 같은 사후 순위 보정을 대체한다.
//  3) 같은 생년월일이면 언제나 같은 점수가 나온다. (재현 가능 = 신뢰의 근거)
