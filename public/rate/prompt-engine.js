const TWELVE_STARS = ['장생','목욕','관대','건록','제왕','쇠','병','사','묘','절','태','양'];
const TWELVE_START = {
  // 일간별 장생지 (지지 인덱스)
  0: 11, // 갑 → 해(亥)에서 장생
  1: 6,  // 을 → 오(午)에서 장생
  2: 2,  // 병 → 인(寅)에서 장생
  3: 9,  // 정 → 유(酉)에서 장생
  4: 2,  // 무 → 인(寅)에서 장생
  5: 9,  // 기 → 유(酉)에서 장생
  6: 5,  // 경 → 사(巳)에서 장생
  7: 0,  // 신 → 자(子)에서 장생
  8: 8,  // 임 → 신(申)에서 장생
  9: 3,  // 계 → 묘(卯)에서 장생
};
const TWELVE_DIR = { // 양간 순행, 음간 역행
  0:1,1:-1,2:1,3:-1,4:1,5:-1,6:1,7:-1,8:1,9:-1
};

function getTwelveStar(dayTG, dz) {
  const start = TWELVE_START[dayTG];
  const dir = TWELVE_DIR[dayTG];
  const idx = ((dz - start) * dir % 12 + 12) % 12;
  return TWELVE_STARS[idx];
}

// 오행 분포 계산 - 가중치 적용 (월지 2.0, 일지 1.5, 월간 1.5, 나머지 1.0)
function calcElemDist(r, hasHour) {
  const W = {Wood:0,Fire:0,Earth:0,Metal:0,Water:0};

  // 천간: 년간 1.0 / 월간 1.5 / 일간 1.0 / 시간 1.0
  W[TG_ELEM[r.yrTG]]    += 1.0;
  W[TG_ELEM[r.monthTG]] += 1.5;
  W[TG_ELEM[r.dayTG]]   += 1.0;
  if (hasHour) W[TG_ELEM[r.hourTG]] += 1.0;

  // 지지: 년지 1.0 / 월지 2.0 / 일지 1.5 / 시지 1.0
  W[DZ_ELEM[r.yrDZ]]    += 1.0;
  W[DZ_ELEM[r.monthDZ]] += 2.0;
  W[DZ_ELEM[r.dayDZ]]   += 1.5;
  if (hasHour) W[DZ_ELEM[r.hourDZ]] += 1.0;

  const total = Object.values(W).reduce((a,b)=>a+b,0);
  return Object.entries(W).map(([k,v]) => `${ELEM_KR[k]}${Math.round(v/total*100)}%`).join(' ');
}

// 지지 관계 계산 (합/충/형/파/해/무관계)
function getBranchRelation(dz1, dz2) {
  // 육합 (六合)
  const LIUHE = [[0,1],[2,11],[3,10],[4,9],[5,8],[6,7]]; // 자축/인해/묘술/진유/사신/오미
  // 충 (六冲) - 6칸 차이
  // 삼형 (三刑)
  const SANXING = [
    [2,5,8],   // 인사신
    [1,4,10],  // 축술미
    [0,3],     // 자묘 (자묘형)
    [6],       // 오 (자형)
    [9],       // 유 (자형)
  ];
  // 파 (六破)
  const LIUPO = [[0,9],[1,10],[2,11],[3,6],[4,7],[5,8]]; // 자유/축술/인해/묘오/진미/사신
  // 해 (六害)
  const LIUHAI = [[0,7],[1,6],[2,9],[3,8],[4,11],[5,10]]; // 자미/축오/인유/묘신/진해/사술

  if (dz1 === dz2) return '자형(自刑)';

  // 충
  if (Math.abs(dz1 - dz2) === 6) return '충(冲)';

  // 육합
  for (const pair of LIUHE) {
    if ((pair[0]===dz1&&pair[1]===dz2)||(pair[0]===dz2&&pair[1]===dz1)) return '합(合)';
  }

  // 삼형
  for (const group of SANXING) {
    if (group.includes(dz1) && group.includes(dz2)) return '형(刑)';
  }

  // 파
  for (const pair of LIUPO) {
    if ((pair[0]===dz1&&pair[1]===dz2)||(pair[0]===dz2&&pair[1]===dz1)) return '파(破)';
  }

  // 해
  for (const pair of LIUHAI) {
    if ((pair[0]===dz1&&pair[1]===dz2)||(pair[0]===dz2&&pair[1]===dz1)) return '해(害)';
  }

  return '무관계';
}

// 내 일간 기준 상대 사주 각 간지의 십성 분포
function getPartnerTenGodDist(myDayTG, pR, hasHour) {
  const pillars = hasHour
    ? [['년간',pR.yrTG],['월간',pR.monthTG],['일간',pR.dayTG],['시간',pR.hourTG]]
    : [['년간',pR.yrTG],['월간',pR.monthTG],['일간',pR.dayTG]];
  const branches = hasHour
    ? [['년지',pR.yrDZ],['월지',pR.monthDZ],['일지',pR.dayDZ],['시지',pR.hourDZ]]
    : [['년지',pR.yrDZ],['월지',pR.monthDZ],['일지',pR.dayDZ]];

  const stemLine = pillars.map(([pos, tg]) => `${pos}→${getTenGod(myDayTG, tg)}`).join(' / ');
  const branchLine = branches.map(([pos, dz]) => {
    const mainTG = DZ_MAIN_TG[dz];
    return `${pos}→${getTenGod(myDayTG, mainTG)}`;
  }).join(' / ');

  // 십성 카운트 집계
  const counter = {};
  [...pillars.map(([,tg])=>getTenGod(myDayTG,tg)),
   ...branches.map(([,dz])=>getTenGod(myDayTG,DZ_MAIN_TG[dz]))
  ].forEach(s => { counter[s]=(counter[s]||0)+1; });

  // 카운트 내림차순 정렬
  const sorted = Object.entries(counter).sort((a,b)=>b[1]-a[1]);
  const distLine = sorted.map(([s,c]) => `${s} ${c}개`).join(' / ');
  const dominantLine = sorted.map(([s]) => s).join(' > ');

  return `천간: ${stemLine}\n지지(지장간): ${branchLine}\n십성 분포: ${distLine}\n대표: ${dominantLine}`;
}

function buildPersonText(label, year, month, day, hour, isMe) {
  const hasHour = hour !== null && hour !== undefined;
  const h = hasHour ? hour : 12;
  const r = calcSaju(year, month, day, h);

  const yearPillar  = `${STEMS_KR[r.yrTG]}${BRANCHES_KR[r.yrDZ]}년`;
  const monthPillar = `${STEMS_KR[r.monthTG]}${BRANCHES_KR[r.monthDZ]}월`;
  const dayPillar   = `${STEMS_KR[r.dayTG]}${BRANCHES_KR[r.dayDZ]}일`;
  const hourPillar  = hasHour ? `${STEMS_KR[r.hourTG]}${BRANCHES_KR[r.hourDZ]}시` : '(시주 미상)';

  const dayMasterHanja = STEM_HANJA[r.dayTG];
  const dayElem = TG_ELEM[r.dayTG];
  const dayYY   = TG_YY[r.dayTG];

  const spouseDesc  = SPOUSE_PALACE_DESC[r.dayDZ];
  const spouseHanja = BRANCH_HANJA[r.dayDZ];

  // 월지
  const monthBranchHanja = BRANCH_HANJA[r.monthDZ];
  const monthBranchElem  = ELEM_KR[DZ_ELEM[r.monthDZ]];

  // 일지 12운성
  const twelveStar = getTwelveStar(r.dayTG, r.dayDZ);

  // 오행 분포
  const elemDist = calcElemDist(r, hasHour);
  const hourNote = hasHour ? '' : ' (시주 미상, 3기둥 기준)';

  const text = `${label}:
사주: ${yearPillar} / ${monthPillar} / ${dayPillar} / ${hourPillar}
일간: ${dayMasterHanja}${ELEM_KR[dayElem]} (${YY_KR[dayYY]}${ELEM_NAME_KR[dayElem]})
월지: ${monthBranchHanja}(${monthBranchElem}) → 성장 환경·사고방식 형성 배경
배우자궁(일지): ${spouseHanja} → ${spouseDesc}
일지 12운성: ${twelveStar}
오행 분포: ${elemDist}${hourNote}`;

  return { text, r, dayElem, dayYY, dayDZ: r.dayDZ };
}

