const SHENG_ME = { Wood:'Water', Fire:'Wood', Earth:'Fire', Metal:'Earth', Water:'Metal' }; // 나를 생하는 오행(인성)
function calcStrength(r, hasHour) {
  const me = TG_ELEM[r.dayTG];
  const support = SHENG_ME[me];
  const isAlly = e => (e === me || e === support); // 비겁 또는 인성
  let ally = 0, tot = 0;
  const add = (e, w) => { tot += w; if (isAlly(e)) ally += w; };
  add(TG_ELEM[r.yrTG],1); add(TG_ELEM[r.monthTG],1); if(hasHour)add(TG_ELEM[r.hourTG],1); // 일간(dayTG)은 주체라 제외
  add(DZ_ELEM[r.yrDZ],1); add(DZ_ELEM[r.monthDZ],2); add(DZ_ELEM[r.dayDZ],1); if(hasHour)add(DZ_ELEM[r.hourDZ],1);
  const ratio = ally / tot;
  if (ratio >= 0.5)  return `신강(身强) — 자기 주관·주도성이 강하고 남에게 잘 안 기댐 (일간 편 ${Math.round(ratio*100)}%)`;
  if (ratio <= 0.32) return `신약(身弱) — 주변 영향을 잘 받고 기대고 싶어함 (일간 편 ${Math.round(ratio*100)}%)`;
  return `중화(中和) — 강약이 균형 (일간 편 ${Math.round(ratio*100)}%)`;
}

function getBranchRelation(dz1, dz2) {
  const LIUHE=[[0,1],[2,11],[3,10],[4,9],[5,8],[6,7]];
  const SANXING=[[2,5,8],[1,4,10],[0,3],[6],[9]];
  const LIUPO=[[0,9],[1,10],[2,11],[3,6],[4,7],[5,8]];
  const LIUHAI=[[0,7],[1,6],[2,9],[3,8],[4,11],[5,10]];
  if(dz1===dz2)return '자형(自刑)';
  if(Math.abs(dz1-dz2)===6)return '충(冲)';
  for(const p of LIUHE)if((p[0]===dz1&&p[1]===dz2)||(p[0]===dz2&&p[1]===dz1))return '합(合)';
  for(const g of SANXING)if(g.includes(dz1)&&g.includes(dz2))return '형(刑)';
  for(const p of LIUPO)if((p[0]===dz1&&p[1]===dz2)||(p[0]===dz2&&p[1]===dz1))return '파(破)';
  for(const p of LIUHAI)if((p[0]===dz1&&p[1]===dz2)||(p[0]===dz2&&p[1]===dz1))return '해(害)';
  return '무관계';
}
