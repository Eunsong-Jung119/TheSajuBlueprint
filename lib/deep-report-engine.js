// myInfo:    { year, month, day, hour, gender }
// target:    { name, year, month, day, hour }
// now:       Date (기본 = 오늘)
function buildDeepPromptMessages(myInfo, target, now) {
  now = now || new Date();
  const nowYear = now.getFullYear();
  const nowMonth = now.getMonth() + 1;

  const genderLabel = myInfo.gender === 'female' ? '여' : '남';
  const myBirth = `${myInfo.year}.${String(myInfo.month).padStart(2,'0')}.${String(myInfo.day).padStart(2,'0')}`;
  const myData = buildPersonText(`나 - ${genderLabel}, ${myBirth}`, myInfo.year, myInfo.month, myInfo.day, myInfo.hour, true);

  const tBirth = `${target.year}.${String(target.month).padStart(2,'0')}.${String(target.day).padStart(2,'0')}`;
  const tHasHour = target.hour !== null && target.hour !== undefined && target.hour !== '';
  const tData = buildPersonText(`상대 - "${target.name}" (${tBirth})`, target.year, target.month, target.day, tHasHour ? target.hour : null, false);

  // 상대 성별 = 사용자 반대로 가정 (이성 연애)
  const partnerGender = myInfo.gender === 'female' ? 'male' : 'female';

  // 상대 십성 (내 일간 기준)
  const tenGodDist = getPartnerTenGodDist(myData.r.dayTG, tData.r, tHasHour);
  const branchRel = getBranchRelation(myData.dayDZ, tData.dayDZ);

  // 현재 세운
  const seun = getAnnualFlow(nowYear, 1);

  // 상대 원국 재성 강약 (Card3: 관심만 vs 실제 행동)
  const wealth = countWealthStar(myData.r.dayTG, tData.r, tHasHour);
  // ↑ 주의: 상대의 연애 행동력은 '상대 자기 일간 기준' 재성이어야 정확
  const tWealth = countWealthStar(tData.r.dayTG, tData.r, tHasHour);

  // 향후 6개월 교차 월운 (배우자궁 관계 보정 포함)
  const monthly = buildMonthlyText(myData.r.dayTG, myInfo.gender, myData.dayDZ, tData.r.dayTG, partnerGender, tData.dayDZ, nowYear, nowMonth);

  const userMessage = `
[분석 대상: ${genderLabel === '여' ? '여성' : '남성'} 사용자 → 상대 "${target.name}"]

${myData.text}

${tData.text}
내 일간 기준 상대 십성:
${tenGodDist}
배우자궁 관계 (내 일지 ↔ 상대 일지): ${branchRel}

[현재 세운] ${seun}

[상대 원국 재성(연애 행동력) 강약] ${tWealth.level} (재성 ${tWealth.cnt}개) — 재성 강하면 관심 생길 때 실제로 움직이고, 없으면 관심만 갖고 행동 안 함

[향후 6개월 교차 월운] (나=${genderLabel}, 상대=${partnerGender === 'male' ? '남' : '여'})
${monthly}

위 정보로 5개 섹션 심층 분석을 JSON으로만 응답해줘.
`.trim();

  return [
    { role: 'system',    content: DEEP_SYSTEM_PROMPT },
    { role: 'user',      content: DEEP_FEW_SHOT_USER },
    { role: 'assistant', content: DEEP_FEW_SHOT_ASSISTANT },
    { role: 'user',      content: userMessage },
  ];
}

